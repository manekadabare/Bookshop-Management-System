    private void newqtyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newqtyActionPerformed
        try {
            double b = Double.parseDouble(bprice.getText());
            double itemqty = Double.parseDouble(newqty.getText());
            
            DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
            Vector v = new Vector();
            if (dtm.getRowCount() == 0) {
                v.add(itemid.getText());
                v.add(batchid.getText());
                v.add(name.getText());
                v.add(newqty.getText());
                v.add(bprice.getText());
                v.add(b * itemqty);
                dtm.addRow(v); 
                addToTable();
            }else{
                for (int j = 0; j < dtm.getRowCount(); j++) {
                    if (jTable2.getValueAt(j, 0).equals(itemid.getText()) & jTable2.getValueAt(j, 1).equals(batchid.getText())) {
                        String q = jTable2.getValueAt(j, 3).toString();
                        double finalqty = Double.parseDouble(q) + itemqty;
                        double finaltot = finalqty * b;
                        jTable2.setValueAt(finalqty, j, 3);
                        jTable2.setValueAt(finaltot, j, 5);
                       addToTable();
                        break;
                    } else if (j == dtm.getRowCount() - 1) {
                        v.add(itemid.getText());
                        v.add(batchid.getText());
                        v.add(name.getText());
                        v.add(newqty.getText());
                        v.add(bprice.getText());
                        v.add(b * itemqty);
                        dtm.addRow(v);
                        addToTable();
                        break;
                    }
                }
            }
           
            getItem();
            getItemID();
            getBatchID();
            clear();
             
            calNetTotal();
        } catch (Exception e) {
            
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_newqtyActionPerformed

