    public void getSupplier() {
        try {
            ResultSet rs=MyDB.search("select * from supplier");
            Vector v=new Vector();
            while (rs.next()) {                
                v.add(rs.getString("supplierid")+" - "+rs.getString("name"));
                supplier.setModel(new DefaultComboBoxModel(v));
            }
        } catch (Exception e) {
        }
    }

    private void getItem() {
        try {
            DefaultTableModel dtm=(DefaultTableModel)jTable1.getModel();
            ResultSet rs=MyDB.search("select * from item inner join batch where item.batchid=batch.batchid");
            dtm.setRowCount(0);
            while (rs.next()) {                
                Vector v=new Vector();
                v.add(rs.getString("itemid"));
                v.add(rs.getString(2));
                v.add(rs.getString("barcode"));
                v.add(rs.getString("name"));
                v.add(rs.getString("qty"));
                v.add(rs.getString("bprice"));
                v.add(rs.getString("sprice"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
        }
    }

    private void getGRNID() {
        try {
            ResultSet rs=MyDB.search("select count(grnid) as x from grn");
            if (rs.next()) {
                int x=Integer.parseInt(rs.getString("x"));
                ++x;
                grnid.setText("G"+x);
            }
        } catch (Exception e) {
        }
    }

