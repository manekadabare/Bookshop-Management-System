    private void searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchKeyReleased
        try {
            DefaultTableModel dtm=(DefaultTableModel)jTable1.getModel();
            ResultSet rs=MyDB.search("select * from supplier where name like '%"+search.getText()+"%'");
            if (rs.next()) {
                if (rs.getString("status").equals("1")) {
                    supplierid.setText(rs.getString("supplierid"));
                name.setText(rs.getString("name"));
                ad1.setText(rs.getString("ad1"));
                ad2.setText(rs.getString("ad2"));
                ad3.setText(rs.getString("ad3"));
                ad4.setText(rs.getString("ad4"));
                rs=MyDB.search("select * from suppliertel");
            dtm.setRowCount(0);
            while (rs.next()) {
                Vector v=new Vector();
                v.add(rs.getString("type"));
                v.add(rs.getString("tel"));
                dtm.addRow(v);
            }
                }
                
            }
            
        } catch (Exception e) {
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_searchKeyReleased

