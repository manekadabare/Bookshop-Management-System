    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        try {
            DefaultTableModel dtm=(DefaultTableModel)jTable1.getModel();
            if (name.getText().isEmpty()|ad1.getText().isEmpty()|ad2.getText().isEmpty()) {
                msg.setText("Fill all the Details!");
            }
            MyDB.iud("insert into supplier values('"+supplierid.getText()+"','"+name.getText()+"','"+ad1.getText()+"',"
                    + "'"+ad2.getText()+"','"+ad3.getText()+"','"+ad4.getText()+"','1')");
            for (int i = 0; i < dtm.getRowCount(); i++) {
                String s1=dtm.getValueAt(i, 0).toString();
                String s2=dtm.getValueAt(i, 1).toString();
                MyDB.iud("insert into suppliertel values('"+s2+"','"+s1+"','"+supplierid.getText()+"')");
            }
           clear();
          
            JOptionPane.showMessageDialog(rootPane, "Supplier Added Successfully!");
    
      
        }catch(NullPointerException npe){
            msg.setText("Fill all the Details!");
        } catch(MySQLIntegrityConstraintViolationException m){
            msg.setText("Information Already Exists!");
        }
        catch (Exception e) {
            e.printStackTrace();
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_submitActionPerformed

