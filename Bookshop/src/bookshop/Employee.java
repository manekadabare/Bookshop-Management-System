    Employee(String s) {
        this();
         try {
             nic.grabFocus();
            update.setVisible(true);
            delete.setVisible(true);
            back.setVisible(true);
            submit.setVisible(false);
            DefaultTableModel dtm=(DefaultTableModel)jTable1.getModel();
            ResultSet rs=MyDB.search("select * from employee where empid='"+s+"'");
            if (rs.next()) {
                empid.setText(rs.getString("empid"));
                nic.setText(rs.getString("nic"));
                title.setSelectedItem(rs.getString("title"));
                fname.setText(rs.getString("fname"));
                lname.setText(rs.getString("lname"));
                fullname.setText(rs.getString("fullname"));
                String gender=rs.getString("gender");
                if (gender.equals("Female")) {
                    female.setSelected(true);
                    male.setSelected(false);
                }
                jDateChooser1.setDate(rs.getDate("dob"));
                status.setSelectedItem(rs.getString("status"));
                email.setText(rs.getString("email"));
                ad1.setText(rs.getString("ad1"));
                ad2.setText(rs.getString("ad2"));
                ad3.setText(rs.getString("ad3"));
                ad4.setText(rs.getString("ad4"));
                path=rs.getString("image");
                File f=new File(path);
                Image i=ImageIO.read(f);
                i=i.getScaledInstance(img.getWidth(), img.getHeight(), Image.SCALE_SMOOTH);
                img.setIcon(new ImageIcon(i));
            }
            rs=MyDB.search("select * from login where empid='"+s+"'");
             if (rs.next()) {
                 String t=rs.getString("type");
                 if (t.equals("Admin")) {
                     admin.setSelected(true);
                     staff.setSelected(false);
                 }
                 username.setText(rs.getString("username"));
                 password.setText(rs.getString("password"));
             }
             rs=MyDB.search("select * from emptel where empid='"+s+"'");
             while (rs.next()) {                 
                 Vector v=new Vector();
                 v.add(rs.getString("type"));
                 v.add(rs.getString("tel"));
                 dtm.addRow(v);
             }
        } catch (Exception e) {
        }
    }

