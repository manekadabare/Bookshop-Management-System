    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        try {
            SimpleDateFormat sdf=new SimpleDateFormat("yyyy-MM-dd");
            String gender=(male.isSelected())?"Male":"Female";
            DefaultTableModel dtm=(DefaultTableModel)jTable1.getModel();
            String pw=new String(password.getPassword());
            String t=(admin.isSelected())?"Admin":"Staff";
            MyDB.iud("update employee set nic='" + nic.getText() + "',title='" + title.getSelectedItem() + "',"
                + "fname='" + fname.getText() + "',lname='" + lname.getText() + "',"
                + "fullname='" + fullname.getText() + "',dob='" + sdf.format(jDateChooser1.getDate()) + "',"
                + "gender='" + gender + "',email='" + email.getText() + "',image='" + path + "',"
                + "ad1='" + ad1.getText() + "',ad2='" + ad2.getText() + "',ad3='" + ad3.getText()+"',"
                + "ad4='"+ad4.getText()+"',status='"+status.getSelectedItem()+"' where "
                + "empid='" + empid.getText() + "' ");
            MyDB.iud("delete from emptel where empid='"+empid.getText()+"'");
            for (int i = 0; i < dtm.getRowCount(); i++) {
                String s1=dtm.getValueAt(i, 0).toString();
                String s2=dtm.getValueAt(i, 1).toString();
                MyDB.iud("insert into emptel values ('"+s2+"','"+s1+"','"+empid.getText()+"')");
            }
            MyDB.iud("update login set username='"+username.getText()+"',password='"+pw+"',"
                + "type='"+t+"' where empid='"+empid.getText()+"'");
            getEmployeeID();
            clear();
            dtm.setRowCount(0);
            JOptionPane.showMessageDialog(rootPane, "Employee Updated Successfully!");
            nic.grabFocus();
            update.setVisible(false);
            submit.setVisible(true);
        } catch (Exception e) {
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_updateActionPerformed

    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        try {
            SimpleDateFormat sdf=new SimpleDateFormat("yyyy-MM-dd");
            String gender=(male.isSelected())?"Male":"Female";
            DefaultTableModel dtm=(DefaultTableModel)jTable1.getModel();
            String pw=new String(password.getPassword());
            String t=(admin.isSelected())?"Admin":"Staff";
            if (!(nic.getText().matches("[0-9]{9}[vVxX]"))) {
                nicmsg.setText("Invalid NIC!");
                nic.setText(null);
                nic.grabFocus();
            }
            if (nic.getText().isEmpty()|fname.getText().isEmpty()|lname.getText().isEmpty()|email.getText().isEmpty()
                |ad1.getText().isEmpty()|ad2.getText().isEmpty()) {
                msg.setText("Fill all the Details");
            }
            ResultSet rs=MyDB.search("select * from login where username='"+username.getText()+"'");
            if (rs.next()) {
                usernamemsg.setText("Username already exists!");
            }
            MyDB.iud("insert into employee values ('"+empid.getText()+"','"+nic.getText()+"','"+title.getSelectedItem()+"',"
                + "'"+fname.getText()+"','"+lname.getText()+"','"+fullname.getText()+"',"
                + "'"+sdf.format(jDateChooser1.getDate())+"','"+gender+"','"+email.getText()+"','"+path+"',"
                + "'"+ad1.getText()+"','"+ad2.getText()+"','"+ad3.getText()+"','"+ad4.getText()+"',"
                + "'"+status.getSelectedItem()+"','1')");
            for (int i = 0; i < dtm.getRowCount(); i++) {
                String s1=dtm.getValueAt(i, 0).toString();
                String s2=dtm.getValueAt(i, 1).toString();
                MyDB.iud("insert into emptel values ('"+s2+"','"+s1+"','"+empid.getText()+"')");
            }
            MyDB.iud("insert into login values('"+username.getText()+"','"+pw+"','"+empid.getText()+"','"+t+"')");
            getEmployeeID();
            clear();
            dtm.setRowCount(0);
            JOptionPane.showMessageDialog(rootPane, "Employee Added Successfully!");
            nic.grabFocus();
        }catch(NullPointerException npe){
            msg.setText("Fill all the Details!");
        }
        catch (Exception e) {
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_submitActionPerformed

