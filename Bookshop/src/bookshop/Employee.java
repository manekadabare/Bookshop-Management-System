 private void getEmployeeID() {
        try {
            ResultSet rs=MyDB.search("select count(empid) as x from employee");
            if (rs.next()) {
                int x=Integer.parseInt(rs.getString("x"));
                x++;
                empid.setText("E"+x);
            }
        } catch (Exception e) {
        }
    }

